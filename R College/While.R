#WHILE 


#printing Natural numbers
{
  a=1
  n=as.integer(readline(prompt="Enter n value : "))
  
  while(a<=n){
     print(a)
     a=a+1
  }
}

#sum of Natural numbers
{
  sum=0
  a=1
  n=as.integer(readline(prompt="Enter n value : "))
  
  while(a<=n){
    
    sum=sum+a
    a=a+1
  }
  print(sum)
}











